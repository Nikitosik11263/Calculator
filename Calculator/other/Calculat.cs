using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Calculator.other
{
    public static class Calculat
    {
        public static double Plus(double fO, double sO) 
        {
            return fO + sO; 
        }
        public static double Minus(double fO, double sO)
        {
            return fO - sO;
        }
        public static double Multiplier(double fO, double sO)
        {
            return fO * sO;
        }
        public static double Fission(double fO, double sO)
        {
            return fO / sO;
        }
        public static double Persent(double fO, double sO)
        {
            return fO % sO;
        }

    }
}
